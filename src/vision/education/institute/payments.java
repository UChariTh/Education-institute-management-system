    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(txt_stid.getText().trim().isEmpty()){
           
            stl1.setText("!Student ID is Empty");
           
        }else if(txt_pamount.getText().trim().isEmpty()){
           
            stl2.setText("!Amount is Empty");
           
        }
        else
        try{

            Class.forName("com.mysql.jdbc.Driver");
            Connection conn= (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/vision education institute","root","");
            String sql= "insert into st_payment(pay_id,st_id,corse_fee,name,nic,course,tel) values(?,?,?,?,?,?,?)";

            PreparedStatement ptstmt= conn.prepareStatement(sql);

            ptstmt.setString(1, txt_pid.getText());
            ptstmt.setString(2, txt_stid.getText());
            ptstmt.setInt(3,Integer.parseInt(txt_pamount.getText()));
            ptstmt.setString(4, txt_stid1.getText());
            ptstmt.setString(5, txt_stid2.getText());
            ptstmt.setString(6, txt_stid3.getText());
            ptstmt.setString(7, txt_stid4.getText());

            ptstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data stores successfully");
            conn.close();

        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


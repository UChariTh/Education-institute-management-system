 public final void ProductCode() {
       
       
        String pcode = txt_stid.getText();
       
        try {
            pst =con.prepareStatement("select * from student where st_id  =?");
            pst.setString(1,pcode);
            rs = pst.executeQuery();
           
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(this,"Lecture ID Not Found","Warning",JOptionPane.WARNING_MESSAGE);
            }
            else
            {
                String st_id=rs.getString("st_id ");
                String name =rs.getString("fname ");
                String nic=rs.getString("nic");
                String course=rs.getString("course");
                String tel=rs.getString("telepohne ");
                
                
                txt_stid.setText(st_id.trim());
                txt_stid1.setText(name.trim());
                txt_stid2.setText(nic.trim());
                txt_stid3.setText(course.trim());
                txt_stid4.setText(tel.trim());
                
               
            }
           
        } catch (SQLException ex) {
            Logger.getLogger(payments.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       
       
       
       
    }

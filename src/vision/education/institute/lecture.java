    public void load() {

        int a;
        try {
            pst=con.prepareStatement("select*from lecture");
            ResultSet rs=pst.executeQuery();
           
            ResultSetMetaData rd=rs.getMetaData();
            a = rd.getColumnCount();
            df =(DefaultTableModel)lecture_table.getModel();
            df.setRowCount(0);
           
            while(rs.next()){
               
                Vector v2 = new Vector();
                for(int i=1; i<=a; i++ ){
                   
                    v2.add(rs.getString("lec_id "));
                    v2.add(rs.getString("fname"));
                    v2.add(rs.getString("lname "));
                    v2.add(rs.getString("nic"));
                    v2.add(rs.getString("gender "));
                    v2.add(rs.getString("telephone "));
                    v2.add(rs.getString("quality"));
                    v2.add(rs.getString("address"));
                    v2.add(rs.getString("dob"));
                   
                   
                }
                df.addRow(v2);
            }
              
           
        } catch (SQLException ex) {
           
            Logger.getLogger(lecture.class.getName()).log(Level.SEVERE, null, ex);
        }
       
     
       
    }


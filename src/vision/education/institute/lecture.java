    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
         if(txt_lefname.getText().trim().isEmpty()){
           
            JOptionPane.showMessageDialog(null,"First Name is Empty");
           
        }else if(txt_lelname.getText().trim().isEmpty()){
           
            JOptionPane.showMessageDialog(null,"Last Name is Empty");
           
        }else if(txt_lenic.getText().trim().isEmpty()){
           
            JOptionPane.showMessageDialog(null,"NIC is Empty");
           
        }else if(txt_leadd.getText().trim().isEmpty()){
           
           JOptionPane.showMessageDialog(null,"Address is Empty");
           
        }else if(txt_ledob.getText().trim().isEmpty()){
           
            JOptionPane.showMessageDialog(null,"Telephone Number is Empty");
           
        }else if(txt_letel.getText().trim().isEmpty()){
           
            JOptionPane.showMessageDialog(null,"Telephone Number is Empty");
           
        }
        else
        
        try {
            
            
            pst = con.prepareStatement("insert into  lecture (lec_id,fname,lname,nic,gender,telephone,quality,address,dob) values (?,?,?,?,?,?,?,?,?)");

          
            pst.setString(1, txt_leid.getText());
            pst.setString(2, txt_lefname.getText());
            pst.setString(3, txt_lelname.getText());
            pst.setString(4, txt_lenic.getText());

            String cos;
            cos =cmb_lcos.getSelectedItem().toString();

            pst.setString(7, cos);

            String gen = null;
            if (rbt_lfemale.isSelected()) {

                gen = rbt_lfemale.getText();
            }
            if (rbt_lmale.isSelected()) {

                gen = rbt_lmale.getText();
            }
            pst.setString(5, gen);

            pst.setString(6, txt_letel.getText());
            pst.setString(8, txt_leadd.getText());
            pst.setString(9, txt_ledob.getText());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data stores successfully");
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
                              
    }//GEN-LAST:event_jButton5ActionPerformed

